package main

import (
	"fmt"
	"os"
	"strings"
)

func main() {
	// get and split the PATH environment variable
	// this only works for the unix-based systems
	words := strings.Split(os.Getenv("PATH"), ":")
	query := os.Args[1:]
queries:
	for _, q := range query {
	search:
		for i, w := range words {
			switch q {
			case "and", "or", "the":
				break search
			}

			if q == w {
				fmt.Printf("#%-2d: %q\n", i+1, w)
				continue queries
			}
		}
	}
}
